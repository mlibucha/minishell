#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*array;
	if (nmemb != 0 && size > SIZE_MAX / nmemb)
		return (NULL);
	array = malloc(nmemb * size);
	if (!array)
		return (NULL);
	ft_bzero(array, nmemb * size);
	return (array);
}
